# Loop through a tuple
car_brands = ("Lamborghini", "Ferrari", "McLaren", "Bugatti")
for brand in car_brands:
    print(brand)
print()

# Loop through the index numbers
for i in range(len(car_brands)):
    print(car_brands[i])
print()

# Using while loop
while j < len(car_brands):
    print(car_brands[j])
    j = j + 1
print()


