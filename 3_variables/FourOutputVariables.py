# Print multiple lines using one print() function
one = "Python"
two = "is"
three = "awesome"
print(one, two, three)
print()

# String concatenation using + symbols.
# Here + works as concatenation operator.
print(one + " " + two + " " + three)

# Summation using + symbols.
# Here + works as mathematical operator.
x = 5
y = 20
print(x + y)

print()
