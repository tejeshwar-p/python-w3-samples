# Python allows you to assign multiple values to multiple variables in one line
# but number of variables should match the number of arguments
first, second, third = "Mango", "Apple", "Orange"
print(first)
print(second)
print(third)

print()

# Same value can be assigned to multiple variables in one line
first = second = third = "Apple"
print(first)
print(second)
print(third)

print()

# Unpack a collection
fruits = ["apple", "banana", "cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)

