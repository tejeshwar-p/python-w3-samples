# To insert characters that are illegal in a string, use and escape character
# example of illegal characters in string -
# txt = "We are the so-called "Vikings" from the north."

# To fix the above problem we use the escape character \"
txt = "We are the so-called \"Vikings\" from the north."
print(txt)

# below are other escape characters used in python
print("\'")     # Single Quote
print("\\")     # Backslash
print("\n")     # New line
print("\r")     # Carriage Return
print("\t")     # Tab
print("\b")     # Backspace
print("\f")     # Form feed
# "\ooo"        # Octal value
# \xhh          # Hex value
